num1 = 100
num2 = 10

puts "1 + 2 = " + (num1 + num2).to_s

# 足し算
result1 = num1 + num2
puts "num1 + num2 = #{result1}"

# 引き算
result2 = num1 - num2
puts "num1 - num2 = #{result2}"

# 掛け算
result3 = num1 * num2
puts "num1 * num2 = #{result3}"

# 割り算
result4 = num1 / num2
puts "num1 / num2 = #{result4}"

# 剰余
result5 = num1 % num2
puts "num1 % num2 = #{result5}"
